import { createSlice } from "@reduxjs/toolkit";

const initialState = {
  users: [],
};

const userSlice = createSlice({
  name: "user",
  initialState,
  reducers: {
    addUsers: (state, { payload }) => {
      state.users = payload;
    },
  },
});

export const { addUsers } = userSlice.actions;
export default userSlice.reducer;
